# 파이썬 기본 데이터 타입
a = 7
b = 7.777
c = True
d = False
e = 'Hello'

print(type(a), type(b), type(c), type(d), type(e))
print(2/4)
print(type(2/4)) # float 자동 타입 변환

print(2**3) # 2의 3제곱
print(10//3) # 2의 3제곱을 나눈 몫
print(10%3) # 2의 3제곱을 나눈 나머지

print(round(2.156)) # 반올림
print(round(2.156, 2)) # 반올림(소수점 두번째 자리까지)
